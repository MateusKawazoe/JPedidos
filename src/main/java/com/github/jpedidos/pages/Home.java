  private void jToggleButton2MouseClicked(java.awt.event.MouseEvent evt) {
    if (jToggleButton2.isSelected()) {
      user.setVisible(true);
      userR.setVisible(false);
    } else {
      user.setVisible(false);
      userR.setVisible(true);
    }
  }

  private void bLogOutMouseClicked(java.awt.event.MouseEvent evt) {
    int dialogResult = JOptionPane.showConfirmDialog(
      null,
      "Would You Like to Save your Previous Note First?",
      "Warning",
      JOptionPane.YES_NO_OPTION
    );

    if (dialogResult == JOptionPane.YES_OPTION) {
      this.dispose();
      new Login();
    }
  }

