  private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {
    try {
      con = userControl.buscarUsuario(jEmails.getSelectedItem().toString());

      if (con.getRs() != null) {
        txtName1.setText(con.getRs().getString("user_name"));
        jEmails1.setSelectedItem(con.getRs().getString("user_type"));
        txtPrice1.setText(con.getRs().getInt("user_phone") + "");
      }
      con.close();
    } catch (Exception e) {
      con.close();
      System.out.println(e);
    }
  }

  private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {
      limparTextFields();
  }

  private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {
    String result = userControl.alterar(
      jEmails.getSelectedItem().toString(),
      Integer.parseInt(txtPrice1.getText()),
      jEmails1.getSelectedItem().toString()
    );
    JOptionPane.showMessageDialog(
      null,
      result,
      "Aviso",
      JOptionPane.INFORMATION_MESSAGE
    );
    limparTextFields();
  }

  private void limparTextFields() {
    txtName1.setText("");
    jEmails1.setSelectedIndex(-1);
    txtPrice1.setText("");
    txtName1.requestFocus();
  }

